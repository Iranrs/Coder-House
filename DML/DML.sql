-- PROCEDURE para rodar a Query 500 vezes
DELIMITER $$
    CREATE PROCEDURE ExecuteQuery500Times()
BEGIN
    DECLARE i INT DEFAULT 0;
    WHILE i < 500 DO
        -- Gerando os dados
       INSERT INTO CONSULTA  
        (ID_PET, ID_VETERINARIO, ID_EXAME, CONSULTA_DATA, CONSULTA_HORA)
		select
            FLOOR(RAND() * 64) + 1,        -- ID_PET: Valores aleatórios entre 1 e 64
            FLOOR(RAND() * 7) + 1,        -- ID_VETERINARIO: Valores aleatórios entre 1 e 7
            FLOOR(RAND() * 12) + 1,        -- ID_EXAME: Valores aleatórios entre 1 e 12
            DATE_ADD('2022-01-01', INTERVAL FLOOR(RAND() * 600) DAY), -- CONSULTA_DATA: Data aleatória a partir de 01.01.2022
            MAKETIME(FLOOR(RAND() * 24), FLOOR(RAND() * 60), 0);     -- CONSULTA_HORA: Hora aleatória entre 00:00 e 23:59
        SET i = i + 1;
    END WHILE;
END$$
DELIMITER ;
-- Chama a procedure para executar a consulta 500 vezes
